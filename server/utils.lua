-- GETFIRSTFREEINDEX
-----------------------------------------------------------------------------------------------------------------------------------------
function GetFirstFreeIndex(table)
    local freeIndex = 0
    local count = 0
    repeat
        count = count + 1
        freeIndex = count
    until not table[freeIndex]

    return freeIndex
end
